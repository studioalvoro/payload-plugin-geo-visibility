import { readdirSync, readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const flagsDir = resolve(__dirname, '..', 'flags')
const outFile = resolve(__dirname, '..', 'src', 'flags-inline.ts')

const files = readdirSync(flagsDir).filter((f) => f.endsWith('.svg')).sort()

const entries = files.map((file) => {
  const code = file.replace('.svg', '')
  const svg = readFileSync(resolve(flagsDir, file), 'utf-8').replace(/`/g, '\\`').replace(/\$/g, '\\$')
  return `  ['${code}', \`${svg}\`]`
})

const content = `// Auto-generated by scripts/generate-flags-map.mjs — do not edit
export const flagsMap = new Map<string, string>([
${entries.join(',\n')},
])
`

writeFileSync(outFile, content)
console.log(`Generated ${files.length} flag entries → src/flags-inline.ts`)
